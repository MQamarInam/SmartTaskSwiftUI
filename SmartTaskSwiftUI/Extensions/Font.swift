import Foundation
import SwiftUI

extension Font {
    static func amsiProBold(size: CGFloat) -> Font {
        .custom("AmsiPro-Bold", size: size)
    }
    
    static func amsiProRegular(size: CGFloat) -> Font {
        .custom("AmsiPro-Regular", size: size)
    }
}
